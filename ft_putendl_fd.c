#include "libft.h"

void    ft_putendl_fd(char *s, int fd)
{
    if (s == 0)
        return;
    while(*s != '\0')
    {
        write(fd, s, 1);
        s++;
    }
    if (*s == '\0')
        write(fd, "\n", 1);
}